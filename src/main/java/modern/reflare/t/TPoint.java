package modern.reflare.t;

public class TPoint {

    public float x;
    public float y;

    public TPoint(){

    }

    public TPoint(final float x, final float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(final float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(final float y) {
        this.y = y;
    }
}
